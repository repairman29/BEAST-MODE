/**
 * Speed test - generate all components
 */
async function speedTest() {
  console.log('üöÄ BEAST MODE Speed Test');
  console.log('='.repeat(60));
  console.log(`üì¶ Generating ${COMPONENTS_TO_GENERATE.length} components + API routes...\n`);

  const startTime = performance.now();
  const results = [];

  // Generate all components in parallel
  const componentPromises = COMPONENTS_TO_GENERATE.map(async (component) => {
    const componentStart = performance.now();
    
    // Generate component
    const componentCode = generateComponent(component);
    const componentPath = path.join(__dirname, '..', 'website', 'components', 'beast-mode', `${component.name}.tsx`);
    
    // Generate API route
    const apiCode = generateAPIRoute(component);
    const apiPath = path.join(__dirname, '..', 'website', 'app', 'api', component.name.toLowerCase(), 'route.ts');
    
    // Write files
    await fs.promises.mkdir(path.dirname(componentPath), { recursive: true });
    await fs.promises.mkdir(path.dirname(apiPath), { recursive: true });
    
    await fs.promises.writeFile(componentPath, componentCode, 'utf8');
    await fs.promises.writeFile(apiPath, apiCode, 'utf8');
    
    const componentTime = performance.now() - componentStart;
    
    return {
      component: component.name,
      componentPath,
      apiPath,
      time: componentTime,
      lines: componentCode.split('\n').length + apiCode.split('\n').length
    };
  });

  const componentResults = await Promise.all(componentPromises);
  const totalTime = performance.now() - startTime;

  // Calculate statistics
  const totalLines = componentResults.reduce((sum, r) => sum + r.lines, 0);
  const avgTime = componentResults.reduce((sum, r) => sum + r.time, 0) / componentResults.length;
  const linesPerSecond = (totalLines / totalTime) * 1000;
  const componentsPerSecond = (COMPONENTS_TO_GENERATE.length / totalTime) * 1000;

  // Display results
  console.log('‚úÖ Generation Complete!\n');
  console.log('üìä Results:');
  console.log('-'.repeat(60));
  
  componentResults.forEach((result, idx) => {
    console.log(`${idx + 1}. ${result.component}`);
    console.log(`   Component: ${result.componentPath.replace(process.cwd(), '.')}`);
    console.log(`   API Route: ${result.apiPath.replace(process.cwd(), '.')}`);
    console.log(`   Time: ${result.time.toFixed(2)}ms`);
    console.log(`   Lines: ${result.lines}`);
    console.log('');
  });

  console.log('üìà Performance Metrics:');
  console.log('-'.repeat(60));
  console.log(`   Total Time: ${totalTime.toFixed(2)}ms (${(totalTime / 1000).toFixed(2)}s)`);
  console.log(`   Components Generated: ${COMPONENTS_TO_GENERATE.length}`);
  console.log(`   Total Lines: ${totalLines}`);
  console.log(`   Average Time per Component: ${avgTime.toFixed(2)}ms`);
  console.log(`   Lines per Second: ${linesPerSecond.toFixed(0)}`);
  console.log(`   Components per Second: ${componentsPerSecond.toFixed(2)}`);
  console.log('');

  // Speed rating
  let rating = 'üêå';
  if (totalTime < 100) rating = '‚ö°‚ö°‚ö°';
  else if (totalTime < 500) rating = '‚ö°‚ö°';
  else if (totalTime < 1000) rating = '‚ö°';
  
  console.log(`üèÜ Speed Rating: ${rating}`);
  console.log('');

  return {
    totalTime,
    totalLines,
    componentsGenerated: COMPONENTS_TO_GENERATE.length,
    linesPerSecond,
    componentsPerSecond,
    results: componentResults
  };
}

if (require.main === module) {
  speedTest().catch(error => {
    console.error('‚ùå Speed test failed:', error);
    process.exit(1);
  });
}

module.exports = { speedTest, generateComponent, generateAPIRoute };
